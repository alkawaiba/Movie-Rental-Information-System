    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if(jTextField5.getText().compareTo("") == 0){
            JOptionPane.showMessageDialog(null, "The search text is empty. Invalid");
        }
        else{
                for(int i=0;i<priceArray.size();i++){
                    int info=Integer.parseInt(priceArray.get(i)[2]);
                    int infoIndex=i;
                    for(int j=i+1; j<priceArray.size();j++){
                        int check=Integer.parseInt(priceArray.get(j)[2]);
                        if(check<info) 
                        {
                            info=check;
                            infoIndex=j;
                        }
                    }
                    String[] temp =priceArray.get(i);
                    priceArray.set(i,priceArray.get(infoIndex)); 
                    priceArray.set(infoIndex,temp);        
                }
                int key =Integer.parseInt(jTextField5.getText());
                int low=0;
                int high=priceArray.size()-1;
                int mid=0; boolean found=false;
                while (low<=high) 
                {
                    mid=(high+low)/2;
                    int val=Integer.parseInt(priceArray.get(mid)[2]);
                    if (key==val) {found=true; break;}
                    else if(key<val) {high=mid-1;}
                    else {low=mid+1;}
                }

                if (found) {
                    jTextField5.setText("");
                    JOptionPane.showMessageDialog(null,"Accessory ID:"+priceArray.get(mid)[0]+"\nAccessory Name: "+priceArray.get(mid)[1]
                    +"\nPrice: "+ priceArray.get(mid)[2]+"\nBrand: " +priceArray.get(mid)[3]+"\nConnectivity Type: "+priceArray.get(mid)[4]+"\nAvailiablity:"+priceArray.get(mid)[5]+"\nFrequency:"+priceArray.get(mid)[6]);
                } else{
                    JOptionPane.showMessageDialog(null,"Sorry! No Records found.");
                }
        }  
        
    
    }//GEN-LAST:event_jButton3ActionPerformed

