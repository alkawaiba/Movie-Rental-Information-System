public void export(){
    String filePath="Data.csv";
    File file=new File(filePath);
    
        try {
            FileWriter FL =new FileWriter(file);
            BufferedWriter bw= new BufferedWriter(FL);
            for(int i=0; i<priceArray.size(); i++){
                for(int j=0; j< jTable1.getColumnCount(); j++){
                    bw.write(priceArray.get(i)[j].toString()+ "," );                   
                }
                bw.newLine();      
            }
            bw.close();
            FL.close();
            
        } catch (IOException ex) {
            Logger.getLogger(Coursework.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showConfirmDialog(this, "Error","Error Message",JOptionPane.ERROR_MESSAGE);
        }
    }
